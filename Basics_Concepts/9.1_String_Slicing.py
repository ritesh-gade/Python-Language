# Slicing :- 

name = "Ritesh Gade"
print(name[0:5])
print(name[2:])   # starting with 2 index end with Final letter
print(name[:6])   # Automatically set Zero at start
print(name[0:-3]) # pytho use the [0:len(name)-3]
print(name[-4:-1])

# Find the length of string:-
print(len(name))

nm = "Harry"
